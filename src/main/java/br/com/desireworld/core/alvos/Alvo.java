package br.com.desireworld.core.alvos;

public abstract class Alvo {
    protected int id;
    protected String nome;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }


}
